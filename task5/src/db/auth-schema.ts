export const connection = pgTable(
  "connection",
  {
    id: text("id").primaryKey(),
    userId: text("user_id")
      .notNull()
      .references(() => user.id, { onDelete: "cascade" }),
    connectedUserId: text("connected_user_id")
      .notNull()
      .references(() => user.id, { onDelete: "cascade" }),
    createdAt: timestamp("created_at").defaultNow().notNull(),
  },
  (table) => [
    index("connection_userId_idx").on(table.userId),
    index("connection_connectedUserId_idx").on(table.connectedUserId),
  ],
);

export const userRelations = relations(user, ({ many }) => ({
  sessions: many(session),
  accounts: many(account),
  connections: many(connection, {
    relationName: "userConnections",
  }),
  connectedTo: many(connection, {
    relationName: "connectedToUser",
  }),
}));

export const sessionRelations = relations(session, ({ one }) => ({
  user: one(user, {
    fields: [session.userId],
    references: [user.id],
  }),
}));

export const accountRelations = relations(account, ({ one }) => ({
  user: one(user, {
    fields: [account.userId],
    references: [user.id],
  }),
}));

export const connectionRelations = relations(connection, ({ one }) => ({
  user: one(user, {
    fields: [connection.userId],
    references: [user.id],
    relationName: "userConnections",
  }),
  connectedUser: one(user, {
    fields: [connection.connectedUserId],
    references: [user.id],
    relationName: "connectedToUser",
  }),
}));
